    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelCari =
        new DefaultTableModel();

modelCari.addColumn("Kode");
modelCari.addColumn("Nama");
modelCari.addColumn("Ukuran Lensa");
modelCari.addColumn("Harga");

try{

    Connection conn =
            Koneksi.getConnection();

    String sql =
    "SELECT * FROM kacamata "
    + "WHERE kode LIKE ? "
    + "OR nama LIKE ?";

    PreparedStatement ps =
            conn.prepareStatement(sql);

    ps.setString(1,
            "%" + txtCari.getText() + "%");

    ps.setString(2,
            "%" + txtCari.getText() + "%");

    ResultSet rs =
            ps.executeQuery();

    while(rs.next()){

        modelCari.addRow(new Object[]{

            rs.getString("kode"),
            rs.getString("nama"),
            rs.getString("ukuran_lensa"),
            rs.getDouble("harga")
        });
    }

    tblKacamata.setModel(modelCari);

}catch(Exception e){

    e.printStackTrace();
}
    }//GEN-LAST:event_btnCariActionPerformed

