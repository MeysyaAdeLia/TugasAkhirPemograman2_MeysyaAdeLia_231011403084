    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        Kacamata k = new Kacamata();

k.setKode(txtKode.getText());
k.setNama(txtNama.getText());
k.setUkuranLensa(txtUkuran.getText());
k.setHarga(Double.parseDouble(
        txtHarga.getText()));

KacamataDAO dao =
        new KacamataDAO();

if(dao.update(k)){

    JOptionPane.showMessageDialog(
            this,
            "Data berhasil diubah");

    tampilData();

}else{

    JOptionPane.showMessageDialog(
            this,
            "Gagal mengubah data");
}
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtKode.getText().isEmpty() ||
   txtNama.getText().isEmpty() ||
   txtUkuran.getText().isEmpty() ||
   txtHarga.getText().isEmpty()){

    JOptionPane.showMessageDialog(
            this,
            "Data tidak boleh kosong");

    return;
}

Kacamata k = new Kacamata();

k.setKode(txtKode.getText());
k.setNama(txtNama.getText());
k.setUkuranLensa(txtUkuran.getText());
k.setHarga(Double.parseDouble(
        txtHarga.getText()));

KacamataDAO dao =
        new KacamataDAO();

if(dao.insert(k)){

    JOptionPane.showMessageDialog(
            this,
            "Data berhasil disimpan");

    tampilData();

}else{

    JOptionPane.showMessageDialog(
            this,
            "Gagal menyimpan data");
}
    }//GEN-LAST:event_btnSimpanActionPerformed

